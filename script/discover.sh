#!/usr/bin/env bash

ARG=$1

echo ""
echo "whois ======================================================================================================================="
echo ""
whois ${ARG} | sed -e '/%/d' -e '/#/d' -e '/./,/^$/!d'

echo ""
echo "dig x ======================================================================================================================="
echo ""
dig -x ${ARG}

echo ""
echo "dig any ====================================================================================================================="
echo ""
dig any @8.8.8.8 ${ARG}

echo ""
echo "machinae ===================================================================================================================="
echo ""
machinae ${ARG}
